class Solution
{
    //Function to return a list of nodes visible from the top view 
    //from left to right in Binary Tree.
    private static class Pair{
        int data, depth;
        Pair(int data, int depth){
            this.data = data;
            this.depth = depth;
        }
    }
    
    static ArrayList<Integer> topView(Node root)
    {
        ArrayList<Integer> ans = new ArrayList<>();
        if(root == null){
            return ans;
        }
        
        TreeMap<Integer, Pair> map = new TreeMap<>();
        store(root, 0, 0, map);
        for(int key: map.keySet()){
            ans.add(map.get(key).data);
        }
        return ans;
        
    }
    
    private static void store(Node root,int hd, int depth, TreeMap<Integer, Pair> map){
        if(root == null){
            return;
        }
        
        Pair curr = map.get(hd);
        if(curr == null){
            curr = new Pair(root.data, depth);
        } else if(curr.depth >= depth){
            curr.data = root.data;
            curr.depth = depth;
        }
        
        map.put(hd, curr);
        store(root.left, hd-1, depth+1, map);
        store(root.right, hd+1, depth+1, map);
    }

}